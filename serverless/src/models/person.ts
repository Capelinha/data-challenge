import { attribute, autoGeneratedHashKey, table } from '@aws/dynamodb-data-mapper-annotations';
import { IsNotEmpty, IsOptional } from 'class-validator';
import * as Moment from 'moment-timezone';

@table('people')
export class Person {
    @autoGeneratedHashKey()
    public personId: string;

    @IsNotEmpty()
    @attribute()
    public firstName: string;

    @IsNotEmpty()
    @attribute()
    public lastName: string;

    @IsNotEmpty()
    @attribute()
    public uidCreated: string;

    @IsNotEmpty()
    @attribute()
    public rg: string;

    @IsNotEmpty()
    @attribute()
    public cpf: string;

    @IsOptional()
    @IsNotEmpty()
    @attribute()
    public cnpj: string;

    @IsOptional()
    @IsNotEmpty()
    @attribute()
    public companyName: string;

    @IsOptional()
    @IsNotEmpty()
    @attribute()
    public reportUrl: string;

    @IsNotEmpty()
    @attribute()
    public searchPages: string;

    @IsOptional()
    @attribute()
    public status: any;

    @attribute({
        defaultProvider: () =>
            Moment()
                .tz('America/Sao_Paulo')
                .valueOf(),
    })
    public createdAt: number;
}